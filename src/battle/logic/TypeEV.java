package battle.logic;

/**
 * The TypeEV enum represents the different types of Effort Values (EVs) that a Pokémon can gain.
 * EVs are used to determine a Pokémon's growth in specific stats through training and battles.
 */
public enum TypeEV {
    HP,       // Effort Values related to the Hit Points (HP) stat.
    ATTACK,   // Effort Values related to the Attack stat.
    DEFENSE,  // Effort Values related to the Defense stat.
    SPEED,    // Effort Values related to the Speed stat.
    SPECIAL;  // Effort Values related to the Special stat.
}

